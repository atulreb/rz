<script>
    jQuery(document).ready(function () {
        jQuery("#contactform").submit(function (e) {
            e.preventDefault(); // avoid to execute the actual submit of the form.
            var url = "<?php echo base_url() ?>landing-pages/security/submitContactForm"; // the script where you handle the form input.
            //alert(url);
            jQuery.ajax({
                type: "POST",
                url: url,
                data: jQuery("#contactform").serialize(), // serializes the form's elements.
                dataType: "json",
                success: function (data) {
                    if (data.status === '0') {
                        jQuery("#rspstatus1").html("<span style='color:red'>" + data.msg + "</span>");
                    } else {
                        jQuery("#rspstatus1").html("<span style='color:green'><b>" + data.msg + "</b></span>");
                        jQuery('#contactform')[0].reset();
                    }
                }
            });


        });
    });
</script>