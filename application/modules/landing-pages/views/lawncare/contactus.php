    <script>
        jQuery("#gform_3").submit(function (e) {
            jQuery(".ajax-loader").css('visibility','visible');
            e.preventDefault(); // avoid to execute the actual submit of the form.
            var url = "<?php echo base_url() ?>landing-pages/lawncare/submitContactUsForm"; // the script where you handle the form input.
            //alert(url);
            jQuery.ajax({
                type: "POST",
                url: url,
                data: jQuery("#gform_3").serialize(), // serializes the form's elements.
                dataType: "json",
                success: function (data) {
                    if (data.status === '0') {
                        jQuery(".ajax-loader").css('visibility','hidden');
                        jQuery("#rsp2status").html("<span style='color:red'>" + data.msg + "</span>");
                        jQuery("#gform_ajax_spinner_3").hide();
                    } else {
                        jQuery(".ajax-loader").css('visibility','hidden');
                        jQuery("#rsp2status").html("<span style='color:green'>" + data.msg + "</span>");
                        jQuery("#gform_ajax_spinner_3").hide();
                        jQuery('#gform_3')[0].reset();
                    }
                }
            });
        });
    </script>