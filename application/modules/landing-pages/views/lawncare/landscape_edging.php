			<h1>Landscape Maintenance & Edging</h1>
<p>RazorClean Lawn Care has been providing outstanding customer service for a number of years. Serving our clients has allowed us to better provide lawn and landscape maintenance and grow as a provider. </p>
<p>Your lawn and landscape is the first thing people see when they come to your home or business. It can also affect the property value of your home when your lawn is mowed, trees and shrubs pruned, and leaves picked up. Your Professional Technician can help you with:</p>
<ul><li>
<li>Lawn Mowing</li>
<li>Leaf Clean Up</li>
<li>Shrub Pruning</li>
<li>Mulching</li>
<li>Weeding & Bed Maintenance</li></ul>
<h1>Lawn Moving</h1>
<p>Prior to mowing, the turf areas will be policed for trash, tree limbs or obstructions. Turf will be completely and evenly mowed at a height most suitable for the species and the site’s specific needs.  We do not bag grass clippings only when requested.  Turfgrass research shows that by using proper mowers, it is better to return clippings to the turf for nutritional replenishment. This process is called Grasscycling and it’s a tremendous help to our environment.</p>

<h1>Trimming</h1>
<p>In areas we cannot reach with a mower, we will string trim the turf to the height of adjacent turf.</p>

<h1>Lawn Edging</h1>
<p>We will edge concrete borders such as walkways, curbs and driveways each mowing during the growing season with a string trimmer.</p>


<h1>Mowing Clean-Up</h1>
<p>All concrete surfaces, patios, decks and parking areas will be blown clean of debris from the landscape maintenance process. This process will include the edges of streets in some applications.</p>


<h1>Leaf & Debris Removal</h1>
<p>We remove all leaves & plant materials from your site.  We utilize mulching type lawn mowers to process leaves from trees on your site.  Fall leaves are mulched and then removed from your property for composting every other week in November and December, with a final clean up from beds in December.</p>



<h1>Landscape Detail Visits</h1>
<p>Shrub beds, natural areas with mulch and ground cover areas will be weeded on an every 4 week schedule, 7 times during the year.  We use Round-Up herbicide on any weed that we can safely apply without injury to ornamental plants.  We hand pull weeds within the canopy of shrubs or ground covers. We use selective herbicides for persistent perennial weeds and grass control as needed. Your bed areas should remain 95% weed free.  There will always be some weeds present in a fertile growing environment. Our intention is to always control weeds with no injury to shrubs, ground covers, or trees</p>

<h1>Tree, Shrub, and Plant Pruning</h1>
<p>It is important to remember that pruning plants is a process, not an event.  There is no one-day of the year that all plants are to be pruned.  Each species has very specific pruning needs based on flower production and the growing season.  First priority is pruning for bloom production, second is structure pruning for plant development and third is space management.  Our employees are trained on the most common landscape plants in Kansas.</p>


<h1>Mulch Replacement</h1>
<p>Java Brown is the hardwood mulch of choice for ornamental shrub beds and natural areas.  Our Mulch will hold its color and needs only to be top dressed once a year in the spring.  It benefits shrub beds by helping to retain moisture, control weeds, prevent erosion, protect from temperature extremes and to improve the appearance of our bed areas.</p>

<h1>Pre-Emergent Weed Control</h1>
<p>Applied under a topdressing of mulch helps prevent weeds from germinating.</p>

<h1>Integrated Pest Management For Your Landscape</h1>
<p>Mowing frequency, mowing height, fertilization and irrigation affect weed & pest control.  Managing these separate cultural practices is called the process of Integrated Pest Management (IPM).  IPM is followed by RazorClean lawn Care to minimize the amount of pesticides used in your landscape. IPM says we will adhere to sound horticultural maintenance practices to keep our lawns and trees & shrubs healthy and growing.</p>
<p>We will monitor your landscape for problems or changes in its appearance.  If a problem is detected, we will first identify the pest.  We will then treat affected areas with the proper pesticides at label rates if cultural controls, such as pruning and irrigation changes will not control damage.  Consult your Lawn Care Technician or Operations Manager if you have questions about pest problems in your landscape.</p>

<h1>Your Lawn and Landscape Maintenance Personnel</h1>
<p>Your Landscape Management Team consists of a Maintenance Crew, an Account Operations Manager and a Lawn Care Technician. You will become familiar with your personnel as we keep the same people assigned to your property a period of 6 months to 1 year. I encourage interaction with your Operations Manager on a routine basis.  Your Operations Manager will be able to discuss scheduling, answer technical horticultural questions and pricing for add on services or improvements to your landscape display.</p>
<p>Our Office Manager will be your initial phone contact at RazorClean Lawn Care.  The Office Manager will be responsible for billing questions, scheduling questions, work records and for passing information to appropriate personnel.  We will be here to insure our Lawn Maintenance Staff provides timely and professional service each day, each month and each year.</p>
