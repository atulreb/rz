<p>There are several types of roaches we deal with daily.  The two most common are the American cockroach and the German cockroach.  Both types present significant problems in a home or business if left untreated.  Unfortunately, because of their breeding cycle, a one-time treatment will not do the trick.  A roach problem can take several months to completely eradicate.  Roaches, like all pests, need food, water, and shelter to survive. The more of these resources that are available to them, the more they thrive and multiply.  What makes things more complicated are that the eggs are immune to product and the babies can stay in their eggs for as long as 3 months.  </p>
