                        jQuery("#rspstatus2").html("<span style='color:red'>" + data.msg + "</span>");
                    } else {
                        jQuery("#rspstatus2").html("<span style='color:green'><b>" + data.msg + "</b></span>");
                        jQuery('#contactform1')[0].reset();
                    }
                }
            });
            return false;


        });
