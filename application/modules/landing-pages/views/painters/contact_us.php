            jQuery.ajax({
                type: "POST",
                url: url,
                data: jQuery("#contactform1").serialize(), // serializes the form's elements.
                dataType: "json",
                success: function (data) {
                    e.preventDefault();
                    jQuery("#contact_sbmit").val("Submit");
